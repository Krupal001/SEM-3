<?php
// Declare two variables
$v1 = 10;
$v2 = 5;

// Equality (==)
$result1 = ($v1 == $v2);

// Inequality (!=)
$result2 = ($v1 != $v2);

// Identical (===)
$result3 = ($v1 === $v2);

// Not Identical (!==)
$result4 = ($v1 !== $v2);

// Greater than (>)
$result5 = ($v1 > $v2);

// Less than (<)
$result6 = ($v1 < $v2);

// Greater than or equal to (>=)
$result7 = ($v1 >= $v2);

// Less than or equal to (<=)
$result8 = ($v1 <= $v2);

// Display the results
echo " == : " . $result1 . "<br>";
echo "Inequality (!=): " . $result2 . "<br>";
echo "Identical (===): " . $result3 . "<br>";
echo "Not Identical (!==): " . $result4 . "<br>";
echo "Greater than (>): " .$result5 . "<br>";
echo "Less than (<): " . $result6 . "<br>";
echo "Greater than or equal to (>=): " . $result7 . "<br>";
echo "Less than or equal to (<=): " .$result8 . "<br>";
?>
